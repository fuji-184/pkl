  <section class="section sejarah">
    <h2 class="section__title">Sejarah SMAN 1 Gedangan</h2>
    <p class="category__description visi"  style="text-align: justify;">“ SMAN 1 Gedangan  menjadi satu diantara sekolah menengah atas yang berstatus negeri dan terletak di  Jalan. Raya Sedati Km.2 Gedangan, Sidoarjo. Sekolah ini awal mulanya bukan bernama SMAN 1 Gedangan melainkan bernama  SMAN 18 Surabaya. Awal mula bergantinya nama itu terjadi akibat SMAN 18 Surabaya berpindah tempat ke suatu daerah yang bernama Gedangan pada Juli 1995. 
      <br>
Pada Juli 1995 SMAN 1 Gedangan dipimpin oleh Drs. Abdul Mukti. Pada saat itu, sekolah ini hanya terdiri atas satu kelas saja, hal ini disebabkan tempat di gedung SGPLB IKIP Surabaya yang digunakan untuk ruangan kelas terbatas. Seiring berjalannya waktu, ruangan kelas dan kelas untuk murid bertambah. 
<br>
Pada 29 Januari 1998,  Menteri Pendidikan dan Kebudayaan Republik Indonesia mengeluarkan surat keputusan yang bernomor 13a/O/1998. Surat itu berisi tentang Pembukaan dan Penegerian Sekolah tahun pelajaran 1996/1997 ditetapkan perubahan SMA Negeri 18 Surabaya berubah menjadi SMA Negeri 1 Gedangan Sidoarjo. 
<br>
Pada tahun yang sama, Menteri Pendidikan dan Kebudayaan Republik Indonesia mengeluarkan surat keputusan yang bernomor 94024/A2.1.2/KP/1998 untuk mengangkat kepala sekolah definitif pertama. Pada 02 Desember 1998 terjadi pengangkatan kepala sekolah secara resmi. Jabatan itu diampu oleh Drs. Djawadi. 
<br>
Selang tiga tahun kemudian, tepatnya pada tanggal Pada tanggal 01 September 2001, Kepala Dinas Pendidikan kabupaten Sidoarjo menerbitkan Surat Tugas No.800/5437/404.3.14/2001 yang berisi pengangkatan Pelaksana Tugas Harian (PLH) Kepala Sekolah SMA Negeri 1 Gedangan yang diampu oleh Dra. Mega Suwarni. <br>
Pada 07 Maret 2002, Bupati Sidoarjo mengeluarkan surat keputusan dengan nomor 821.2/37/404.4.5/2002, surat itu berisi tentang pengangkatan Drs. Imam Mulyono sebagai Kepala SMA Negeri 1 Gedangan.  Selang dua tahun kemudian tepatnya pada tanggal 17 Februari 2004, Bupati Sidoarjo menerbitkan surat keputusan lagi dengan nomor 821.2/172/404.4.5/2004. Surat itu berisi tentang pengangkatan Drs. Sukemad sebagai Kepala SMA Negeri 1 Gedangan. 
<br>
Pada 02 Februari 2006, Bupati Sidoarjo mengeluarkan keputusan surat dengan nomor 821.2/022/404.4.5/2006, surat itu berisi mutasi kepala Sekolah Menengah Atas (SMA) menugaskan Drs. Sulaiman Suwarto, M.Pd sebagai Kepala SMA Negeri 1 Gedangan. <br>
Pada 09 Februari 2010, Bupati Sidoarjo juga mengeluarkan surat keputusan dengan nomor 821.2/019/404.6.1/2010, surat itu berisi mutasi kepala Sekolah Menengah Atas (SMA) menugaskan Dra. Sri Mudjajanti, M.Pd sebagai Kepala SMA Negeri 1 Gedangan.Selang dua tahun kemudian, Bupati Sidoarjo mengeluarkan surat keputusan dengan nomor 821.2/169/404.6.1/2012. Surat itu berisi tentang mutasi Kepala Sekolah Menegah Atas (SMA) menugaskan Dra. Lilik Esparlin, M.Si sebagai Kepala SMA Negeri 1 Gedangan. Dewasa ini, SMAN 1 Gedangan dipimpin oleh Dr. H. Panayo, M.Pd.
 ”</p>
